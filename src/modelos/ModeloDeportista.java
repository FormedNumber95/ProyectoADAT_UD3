package modelos;

public class ModeloDeportista {
	
	String nombreDeportista;
	char sexo;
	int altura;
	float peso;
	
	public ModeloDeportista(String nombreDeportista, char sexo, int altura, float peso) {
		this.nombreDeportista = nombreDeportista;
		this.sexo = sexo;
		this.altura = altura;
		this.peso = peso;
	}
	
}
