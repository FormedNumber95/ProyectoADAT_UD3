package modelos;

public class ModeloOlimpiada {
	
	String nombreOlimpiada;
	int anio;
	String temporada;
	String ciudad;
	
	public ModeloOlimpiada(String nombreOlimpiada, int anio, String temporada, String ciudad) {
		this.nombreOlimpiada = nombreOlimpiada;
		this.anio = anio;
		this.temporada = temporada;
		this.ciudad = ciudad;
	}
	
}
